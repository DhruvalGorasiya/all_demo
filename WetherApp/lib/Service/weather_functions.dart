String month(int a) {
  if (a == 1) {
    return 'Jan';
  } else if (a == 2) {
    return 'Feb';
  } else if (a == 3) {
    return 'Mar';
  } else if (a == 4) {
    return 'Apr';
  } else if (a == 5) {
    return 'May';
  } else if (a == 6) {
    return 'Jun';
  } else if (a == 7) {
    return 'Jul';
  } else if (a == 8) {
    return 'Aug';
  } else if (a == 9) {
    return 'Sep';
  } else if (a == 10) {
    return 'Oct';
  } else if (a == 11) {
    return 'Nov';
  } else {
    return 'Dec';
  }
}

